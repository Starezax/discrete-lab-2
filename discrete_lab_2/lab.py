def read_file(filename):
    with open(filename, 'r', encoding = 'utf-8') as f:
        content = f.readlines()[1:-1]
    edges = set()
    vertex = []

    for line in content:
        line = line.strip().strip(';').split(' -> ')
        edge_1, edge_2 = int(line[0]), int(line[1])
        edges.add(edge_1)
        edges.add(edge_2)
        vertex.append((edge_1, edge_2))

    return vertex, edges

